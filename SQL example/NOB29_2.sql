-- EMPLOYEE 테이블에서 부서코드가 D5인 직원의 보너스 포함 연봉 조회
SELECT EMP_NAME, DEPT_CODE, SALARY, SALARY +(SALARY*(NVL(BONUS,0)))AS "보너스 + 연봉" 
FROM EMPLOYEE
WHERE DEPT_CODE = 'D5';
----------------------------------------------------------------------------
-- 사원명, 부서코드, 부서명 조회

SELECT EMP_NAME, DEPT_CODE, DEPT_TITLE
FROM EMPLOYEE JOIN DEPARTMENT
ON DEPT_CODE = DEPT_ID
ORDER BY DEPT_ID;




