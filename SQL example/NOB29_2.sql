-- 사원번호, 사원명, 주민등록번호, 성별(남, 여)로 조회

SELECT EMP_ID, EMP_NAME, EMP_NO,
DECODE(SUBSTR(EMP_NO,8,1), '1','남','여') AS 성별
FROM EMPLOYEE;
----------------------------------------------------------------------------
-- 'J2' 또는 'J7' 직급코드 중 급여를 2000000보다 많이 받는 사원의 이름, 급여, 직급코드 조회
SELECT EMP_NAME,SALARY, JOB_CODE
FROM EMPLOYEE
WHERE JOB_CODE = 'J7' AND SALARY > 2000000 OR JOB_CODE = 'J2' AND SALARY > 2000000 ;

